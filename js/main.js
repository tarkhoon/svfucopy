let hovermenu = document.getElementById('menu1'); //Получение выпадающего меню 1
    hovermenu.onmouseover = over; //Вызов функции over при наведении
    hovermenu.onmouseout = hide; //Вызов функции hide при наведении
var display = document.getElementById('menu1'); //получение элемента menu1

function over(){
    display.classList.add('hs-sub-menu-opened'); //Добавление класса show к элементу (показывает меню)
}

function hide(){
    display.classList.remove('hs-sub-menu-opened');//Удаление класса show к элементу (скрывает меню)
}

